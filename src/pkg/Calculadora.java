package pkg;

public class Calculadora {

	public static Integer Suma(int i, int j) {
		
		return i+j;
	}

	public static Integer Resta(int i, int j) {
		
		return i-j;
	}

	public static Integer Multiplica(int i, int j) {
		
		return i*j;
	}

	public static Integer Divide(int i, int j) {
		
		return i/j;
	}

}
